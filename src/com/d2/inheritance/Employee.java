package com.d2.inheritance;

class Employee {
	int employeeid;
	String name;
	String email;
	String phoneNumber;
	public Employee(int employeeid, String name, String email, String phoneNumber) {
		super();
		this.employeeid = employeeid;
		this.name = name;
		this.email = email;
		this.phoneNumber = phoneNumber;
	}
	
	

}
